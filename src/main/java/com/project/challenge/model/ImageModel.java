package com.project.challenge.model;

/**
 * The Class ImageModel.
 */
public class ImageModel {

	/** The id. */
	private String id;

	/**
	 * Instantiates a new image model.
	 */
	public ImageModel() {
		super();
	}

	/**
	 * Instantiates a new image model.
	 *
	 * @param id the id
	 */
	public ImageModel(String id) {
		super();
		this.id = id;
	}

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(String id) {
		this.id = id;
	}

}
